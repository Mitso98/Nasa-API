const mongoose = require("mongoose");
const { RequestSchema } = require("./Request");

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  isVerified: {
    type: Boolean,
    default: false,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  favorites: [RequestSchema],
});

module.exports = mongoose.model("User", UserSchema);
